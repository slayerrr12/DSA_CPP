#include <bits/stdc++.h>

using namespace std;

/**
 * The main function is the entry point of the program.
 * It demonstrates the usage of bitwise operators.
 */
int main()
{

    int  a = 3;
    int b = 4;

    cout << (a&b) << endl; // 2
    cout << (a | b )<< endl ; //3
    cout << (a ^ b) << endl; //1








return 0;
}